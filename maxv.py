from mpmath import mp, mpf, sqrt, log10, nstr

# Устанавливаем очень высокую точность
mp.dps = 1000

# Константы
c = mpf('299792458')  # скорость света в м/с
m_p = mpf('1.67262192369e-27')  # масса протона в кг
E_universe = mpf('1e68')  # энергия Вселенной в Дж

# Шаг 1: Расчет гамма-фактора
gamma = E_universe / (m_p * c**2) + 1

# Шаг 2: Расчет 1 - v^2/c^2 напрямую
one_minus_v2_c2 = 1 / gamma**2

# Шаг 3: Расчет v/c
v_c = sqrt(1 - one_minus_v2_c2)

# Шаг 4: Расчет v
v = v_c * c

# Шаг 5: Расчет разницы между c и v
diff = c - v

# Вывод результатов
print(f"Гамма-фактор: {nstr(gamma, 10)}")
print(f"v/c: {nstr(v_c, 110)}")
print(f"Скорость протона: {nstr(v, 20)} м/с")
print(f"Разница между скоростью света и скоростью протона: {nstr(diff, 10)} м/с")
print(f"log10 разницы: {nstr(log10(diff), 10)}")
